    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        try{
            MySQLConnect myc = new MySQLConnect();
            Connection con = (Connection) myc.getConn();
            String saveQry = "DELETE FROM `company` WHERE CompanyID = ?";
            PreparedStatement pat = con.prepareStatement(saveQry);
            pat.setInt(1, Integer.parseInt(CompanyID.getText()));
            
            pat.executeUpdate();
            
            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,"User not inserted/saved exception : "+e);
        }
        showTableData();

    }//GEN-LAST:event_DeleteActionPerformed

    public void showTableData(){
        try{
            MySQLConnect myc = new MySQLConnect();
            Connection con = (Connection) myc.getConn();
            String saveQry = "SELECT * FROM `company` ";
            PreparedStatement pat = con.prepareStatement(saveQry);
            ResultSet rs = pat.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
    }
