    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        try{
            MySQLConnect myc = new MySQLConnect();
            Connection con = (Connection) myc.getConn();
            String saveQry = "insert into company(CompanyID, Name, Description, StockPrices, AvailableShares,PerformanceRatio) values(?,?,?,?,?,?)";
            PreparedStatement pat = con.prepareStatement(saveQry);
            pat.setInt(1, Integer.parseInt(CompanyID.getText()));
            pat.setString(2, Name.getText());
            pat.setString(3, Desc.getText());
            pat.setFloat(4, Float.parseFloat(StockP.getText()));
            pat.setFloat(5, Float.parseFloat(AvailS.getText()));
            pat.setFloat(6, Float.parseFloat(PerfR.getText()));
            pat.executeUpdate();
            
            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,"User not inserted/saved exception : "+e);
        }
        showTableData();

    }//GEN-LAST:event_AddActionPerformed

